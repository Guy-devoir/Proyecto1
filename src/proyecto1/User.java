
package proyecto1;


public class User {
    protected String Name;
    protected String Password;

    
    
    public void setName(String Name) {
        this.Name = Name;
    }

    public void setPassword(String Password) {
        this.Password = Password;
    }

    public String getName() {
        return Name;
    }

    public String getPassword() {
        return Password;
    }



    
}
